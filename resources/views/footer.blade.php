        setInterval(function () {
        var CurrentDate = new Date().getTime();
        var DayRemaining = DateExpired - CurrentDate;
        var days = Math.floor(DayRemaining / (1000 * 60 * 60 * 24));
        var hours = Math.floor(DayRemaining / (1000 * 60 * 60));
